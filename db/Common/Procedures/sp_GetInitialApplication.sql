create or alter procedure Common.sp_GetInitialApplication(@ID	uniqueidentifier)
AS
	select  a.CREATION_DATE,
		    a.SOURCE_ID,
		 	a.ID,
			a.LOAN_TYPE_ID,
			a.STATUS as STATUS_ID,
			a.CUSTOMER_USER_ID,
			a.SHOP_USER_ID,
			a.INITIAL_AMOUNT,
			a.CURRENCY_CODE,
			a.REFUSAL_REASON,
			a.MANUAL_REASON,
			a.IS_NEW_CARD,
			a.GIVE_ACCOUNT,
			a.GIVE_CARD,
			a.FIRST_NAME,
			a.LAST_NAME,
			a.PATRONYMIC_NAME,
			a.BIRTH_DATE,
			a.SOCIAL_CARD_NUMBER,
			a.DOCUMENT_TYPE_CODE,
			a.DOCUMENT_NUMBER,
			a.DOCUMENT_GIVEN_BY,
			a.DOCUMENT_GIVEN_DATE,
			a.DOCUMENT_EXPIRY_DATE,
			a.ORGANIZATION_ACTIVITY_CODE,
			a.IS_REFINANCING,
			a.MOBILE_PHONE_1,
			a.PRODUCT_CATEGORY_CODE,
			a.LOAN_TEMPLATE_CODE,
			a.PRODUCT_TOTAL_AMOUNT,
			a.HAS_BANK_CARD,
			a.CLIENT_CODE,
			a.IS_DATA_COMPLETE,
			a.ISN,
			a.PARTNER_COMPANY_CODE,
			u.ONBOARDING_ID,
			a.UNIVERSITY_CODE,
			a.UNIVERSITY_FACULTY,
			a.UNIVERSITY_YEAR
	from Common.APPLICATION a
	left join Common.CUSTOMER_USER u
		on a.CUSTOMER_USER_ID=u.APPLICATION_USER_ID
	where a.ID = @ID
GO
